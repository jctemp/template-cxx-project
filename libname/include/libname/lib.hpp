#ifndef LIBNAME_LIB_HPP
#define LIBNAME_LIB_HPP

#include <stdint.h>
/**
 * @brief Add two number together.
 *
 * @param a first normal integer.
 * @param b second normal interger.
 * @return the sum of a and b as integer.
 */
extern int32_t add(int32_t a, int32_t b);

/**
 * @brief Sub two number together.
 *
 * @param a first normal integer.
 * @param b second normal interger.
 * @return the diff. of a and b as integer.
 */
extern int32_t sub(int32_t a, int32_t b);

#endif // LIBNAME_LIB_HPP
